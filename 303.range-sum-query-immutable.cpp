class NumArray
{
  public:
    NumArray(const vector<int> &nums)
    {
        vec = nums;
        for (int i = 1; i < vec.size(); i++)
        {
            vec[i] += vec[i - 1];
        }
    }
    int sumRange(int i, int j)
    {
        return i == 0 ? vec[j] : vec[j] - vec[i - 1];
    }

  private:
    vector<int> vec;
};

